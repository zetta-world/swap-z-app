// Schema/shape comes from `en` once it's defined; other locales must match.
type Schema = typeof en;

// ─── Helper: a deep-key path generator ────────────────────────────────────
type Prev = [never, 0, 1, 2, 3, 4, 5];
type Path<T, D extends number = 4> = D extends 0 ? never :
  T extends object
    ? { [K in keyof T]: K extends string
        ? T[K] extends string
          ? K
          : `${K}.${Path<T[K], Prev[D]>}`
        : never }[keyof T]
    : never;

export type MessageKey = Path<Schema>;

