/**
 * Z-SWAP i18n catalog — single source of truth for every user-facing string
 * in the platform. Organized by namespace (common, swap, zion, etc.) so each
 * component pulls from a tight scope.
 *
 * Structure:
 *   messages[lang][namespace][key] = "translated string"
 *
 * Variables interpolate with {name} syntax:
 *   t("swap.balance", { amount: "1.4" }) → "Balance: 1.4"
 *
 * Adding a new string:
 *   1. Add it to the `en` block with the right namespace
 *   2. Add the same key to pt / es / zh
 *   3. The MessageKey type below enforces all locales stay in sync
 *
 * Translation guidelines:
 *   • Keep token tickers (ETH, USDT, BTC) and protocol names in English
 *   • Keep action verbs short ("Swap" → "Trocar" / "Intercambiar" / "兑换")
 *   • Keep CTAs imperative
 *   • Chinese uses Simplified (Mainland) — common DEX/CEX terminology
 */

